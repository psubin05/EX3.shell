#!/bin/sh

echo "리눅스가 재밌나요? [Yes/no/하나도 안 재밌어요/좀 그래요]"
read input

# 리눅스가 재밌나요? 질문에 대한 다양한 응답 처리
case $input in
    [Yy]*)
        echo "Yes, 리눅스는 정말 재밌죠! 다양한 기능을 제공해줘서 좋아요!"
        ;;
    [Nn]*)
        echo "No, 아쉬운 걸요. 조금 더 직관적일 수 있으면 좋겠어요."
        ;;
    "하나도 안 재밌어요")
        echo "오, 리눅스는 처음엔 어려울 수 있지만, 점점 재미있어져요!"
        ;;
    "좀 그래요")
        echo "음, 리눅스는 처음엔 적응이 필요하지만, 점차 편리한 점들이 많아요."
        ;;
    "진짜 좋아해요")
        echo "대단해요! 리눅스의 강력한 커스터마이징 기능과 오픈소스 생태계를 즐기고 있군요!"
        ;;
    "리눅스는 최악이에요")
        echo "와, 그렇게 강하게 느끼셨다니... 어떤 점이 불편했나요?"
        ;;
    "헷갈려요")
        echo "리눅스는 처음엔 어려울 수 있지만, 배우면 정말 유용한 시스템이에요!"
        ;;
    "조금 좋아해요")
        echo "리눅스가 조금 재밌나요? 점차 더 많은 기능들을 배우게 될 거예요."
        ;;
    *)
        echo "죄송합니다만, 'Yes', 'No', '하나도 안 재밌어요', '좀 그래요'와 같은 답변을 해주세요."
        ;;
esac

